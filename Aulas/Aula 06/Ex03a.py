valor = int(input('Digite até que numero par vc que contar: '))

i = 0

while i <= valor:
    if i % 2 == 0:
        print(i)
    i += 1    

#Esse programa imprime os somente os valores para a partir de 0 até o valor informado pelo usuario. 
